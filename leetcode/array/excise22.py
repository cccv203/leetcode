class Solution:
    # 返回从尾部到头部的列表值序列，例如[1,2,3]
    def printListFromTailToHead(self, listNode):
        # write code here
        if listNode:
            node = []
            while(listNode.next != None):
                node.append(listNode.val)
                listNode = listNode.next
            node.append(listNode.val)
            return node[-1::-1]
        return []





